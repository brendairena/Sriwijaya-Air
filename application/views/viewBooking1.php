        html += "<input type='hidden' name='id' value='" + values[i] + "'/>";
        html += "<input type='hidden' name='action' value='NewsHeader'/>";
        html += "</form><script type='text/javascript'>document.getElementById(\"formid\").submit()</" + "script></body></html>";
        $('#goto').html(html);
    
    }

